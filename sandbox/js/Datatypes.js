		// Dispel active effects
		dispel(beneficial, max){

			var all = this.effects.slice();
			for(var i =0; i<all.length && (max > 0 || max === -1); ++i){
				var fx = all[i];
				if(
					(all.beneficial && beneficial) ||
					(!all.beneficial && !beneficial)
				){
					this.removeEffect(fx.UUID, false);
					if(max !== -1)
						--max;
				}
			}

		}

