				out.modified = this.modified;
				out.experience = this.experience;
				out.unspent_points = this.unspent_points;
				out.abilities_unlocked = this.abilities_unlocked;
				out.cash = this.cash;
				out.armor_unlocked = this.armor_unlocked;
